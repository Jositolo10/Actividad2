package main.java.jose;

public class OperacionesActividadJose {
	
    public static Double resta(Double valor1, Double valor2) {
        Double resultado;
        
        resultado = valor1 - valor2;
        
        return resultado;
    }
    
    public static Double multiplicacion(Double valor1, Double valor2) {
    	
    	Double resultado;
    	
    	resultado = valor1 * valor2;
    	
    	return resultado;
    	
    }

}
